function insertAlphabetically(obj, newEntries) {
  const merged = { ...obj, ...newEntries };
  return Object.fromEntries(Object.entries(merged).sort(([a], [b]) => a.localeCompare(b)));
}

const files = readdirSync(messagesDir).filter(
  f => f.endsWith('.json') && f !== 'en-US.json',
);

let updated = 0;

for (const fname of files) {
  const locale = fname.replace('.json', '');
  const filePath = join(messagesDir, fname);
  const data = JSON.parse(readFileSync(filePath, 'utf8'));

  const localeTranslations = translations[locale] || {};

  // Merge universal + locale-specific label entries
  const newLabelEntries = {
    ...universal.label,
    ...(localeTranslations.label || {}),
  };

  // Merge message entries
  const newMessageEntries = localeTranslations.message || {};

  // Only add keys that are actually missing
  const missingLabels = Object.fromEntries(
    Object.entries(newLabelEntries).filter(([k]) => !(k in (data.label || {}))),
  );
  const missingMessages = Object.fromEntries(
    Object.entries(newMessageEntries).filter(([k]) => !(k in (data.message || {}))),
  );

  if (Object.keys(missingLabels).length === 0 && Object.keys(missingMessages).length === 0) {
    continue;
  }

  if (Object.keys(missingLabels).length > 0) {
    data.label = insertAlphabetically(data.label || {}, missingLabels);
  }
  if (Object.keys(missingMessages).length > 0) {
    data.message = insertAlphabetically(data.message || {}, missingMessages);
  }

  writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
  console.log(`✓ ${fname}: added ${Object.keys(missingLabels).length} labels, ${Object.keys(missingMessages).length} messages`);
  updated++;
}

console.log(`\nDone. Updated ${updated} files.`);
